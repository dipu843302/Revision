package OOPS;

public class Incapsulation {
    private int age;
    private String name;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public static void main(String[] args) {
        Incapsulation ob1=new Incapsulation();
       ob1.setAge(10);

    }
}
